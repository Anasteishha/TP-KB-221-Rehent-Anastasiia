# Функція для додавання чисел
def add(x, y):
    return x + y

# Функція для віднімання чисел
def subtract(x, y):
    return x - y

# Функція для множення чисел
def multiply(x, y):
    return x * y

# Функція для ділення чисел
def divide(x, y):
    if y == 0:
        return "Ділення на нуль неможливе"
    return x / y
